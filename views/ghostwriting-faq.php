									<div class="entry-content">
										<p>The internet is littered with online writing and editing services, making it nearly impossible to sift through the scams and find a legitimate and trustworthy service. Here are some questions to ask when you’re vetting companies to ensure that you pick a reliable firm for all of your writing and editing needs:</p>
<ol>
<li><b>Is the price right?</b> If it seems too good to be true, it probably is. Developing a high-quality book can take hundreds of hours of work and requires a high level of expertise—don’t be tempted by third-rate writers who market “affordable” prices.</li>
<li><b>Who owns the rights to the book?</b> It’s your book, which means that you should own the rights to your content. Be wary of ghostwriters asking for partial rights to your material.</li>
<li><b>What is the payment arrangement?</b> You should always pay for ghostwriting services in installments—never 100% upfront. After the initial deposit, you should make subsequent payments only if you’re happy with what the ghostwriter has written up to that point. You should have clearly defined milestones for payments to ensure that you and your writer know what to expect.</li>
<li><b>Who’s in charge?</b> Working with a ghostwriter is a collaborative effort, but you should have the final say on all content decisions. If a writer is unwilling to put your needs and requests first, then it’s not going to be your book—it will be theirs.</li>
<li><b>Is the service confidential?</b> This is YOUR book. A ghostwriter’s name should never appear on the completed manuscript unless you want it to be there.</li>
</ol>
<p>Whatever service you decide to use, be sure to protect yourself. Make certain that you are working with a reputable company with experience.</p>
