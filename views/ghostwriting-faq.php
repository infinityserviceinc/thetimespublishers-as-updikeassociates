<p>A book proposal is quite similar to a query letter, except that it is longer and generally includes a synopsis or outline of the book, chapter summaries, detailed author bio, marketing platform/strategy, and a market analysis. Book proposals are generally used for nonfiction books and can be submitted for a manuscript that is not yet complete.</p>
<p>In general, authors of fiction and memoir/autobiography will submit a query letter and authors of nonfiction will submit book proposals.</p>
