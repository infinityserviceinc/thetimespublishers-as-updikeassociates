									<h3><strong><a target="_blank" href="contact-ghostwriting-services/" rel="noopener">Grammar Girl</a>
</strong></h3>
									<p>Grammar Girl and its accompanying podcast, both created by writer and editor extraordinaire Mignon Fogarty, are excellent resources when faced with grammatical questions and other “nuts and bolts” writing issues. The site’s content is clear, fun, and includes plenty of mnemonic devices to help you remember basic rules of grammar. Fogarty addresses common conundrums—such as when to use “who” or “whom” and “lay” or “lie”—in ways that make the principles simple and easy to understand. The site also features a highly effective search function that allows you to quickly find the exact tip you need, as well other related tips.</p>
