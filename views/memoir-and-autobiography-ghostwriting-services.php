<p>Simply put, an autobiography tells the entire story of the author’s life, while a memoir tells a specific story from parts of the author’s life. A memoir might address a particular period of time or phase of life that holds significance, or events that are all related in some way. An autobiography generally covers all aspects of the author’s life up to the time it is written, though some experiences may be given more or less emphasis to convey the desired theme or overarching message.</p>
<p>When choosing whether to write a memoir or an autobiography, consider whether you would like to write your life story, from the beginning to the present day, or whether you would like to focus in on a particular period of time.</p>
