													<div class="body-content body">
														<div class="entry-content">
															<p>A traditional publisher will purchase the rights to publish your book and is then responsible for the final look of the book, the printing, and the marketing. With self-publishing, you pay a publisher to publish and print your book.</p>
<p>There are challenges and benefits associated with both traditional and self-publishing. For authors who sign with a traditional publisher, there is generally guaranteed payment and the sales and earning potential is significantly higher than self-publishing. However, the rights to their book are no longer their own and the publisher may demand various changes to the manuscript that the author may not be completely comfortable with. Self-publishing allows authors more freedom, but on the downside, there is no assurance that their book will sell.</p>
<p>Traditional publishing also generally involves a large commitment of time and effort (and thick skin!) to get a literary agent and publishing deal. In return, authors gain increased visibility and the publisher covers the distribution and marketing costs. Books printed through traditional publishing are guaranteed to be professional quality.</p>
<p>Many first-time authors use self-publishing as a means to prove their marketability to agents and publishers and secure a contract for their subsequent works. More and more, self-publishing is becoming the most common form of publication.</p>
