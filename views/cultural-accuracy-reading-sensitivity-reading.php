															<h2 style="text-align: left;">Why It&#8217;s Important for Your Book</h2>
<hr />
<p style="text-align: left;"><span style="font-weight: 400;">Many publishers are now looking for these critical analyses for manuscripts in order to release culturally accurate books. This line of work arose in response to events and grassroots movements such as #WeNeedDiverseBooks, #MeToo, and #BlackLivesMatter, as the public demands fair and accurate representation, justice, and equity for readers from diverse backgrounds, as well as improved publishing ethics.</span></p>
<p style="text-align: left;"><span style="font-weight: 400;">Several titles have been retracted, condemned, or even canceled entirely in the last decade due to insensitive or harmful content.</span></p>
<p style="text-align: left;"><span style="font-weight: 400;">Our Cultural Accuracy Reading service allows a professional editor who specializes in identifying potential cultural accuracy issues to review and vet your manuscript.</span></p>
<p style="text-align: left;"><span style="font-weight: 400;">The feedback you will receive is not intended to censor or condemn your book, but rather to empower you to make decisions about it.</span></p>
