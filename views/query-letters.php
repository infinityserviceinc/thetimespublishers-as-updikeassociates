															<p>While there is no magic formula for the perfect query letter (in fact, the more personalized each letter is, the better), there are certain components that should generally be included.</p>
<p>First and foremost, a query letter is never longer than one page. Within that page, you must be sure to include a hook (a one- or two-line attention-grabbing statement that conveys the essence of your book), a brief bio that describes your credentials as an author, an explanation of why you think your book is a good fit for the agent or publisher, and last but not least, basic info about your book (such as the title, genre, and word count).</p>
<p>In addition to these specific elements, be sure to personalize, personalize, personalize! Have you attended a workshop with the literary agent or one of their associates? Have you studied one of their published authors? Did you hear the president of their company speak at a conference? Include anything you can that shows you’ve researched the agent or publisher, that you know their work, and that your book would be a good fit within their existing markets and business model.</p>
<p>A book proposal is quite similar to a query letter, except that it is longer and generally includes a synopsis or outline of the book, chapter summaries, detailed author bio, marketing platform/strategy, and a market analysis. Book proposals are generally used for nonfiction books and can be submitted for a manuscript that is not yet complete.</p>
<p>In general, authors of fiction and memoir/autobiography will submit a query letter and authors of nonfiction will submit book proposals.</p>
