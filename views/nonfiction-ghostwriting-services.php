<ol>
<li><b>Is the price right?</b> If it seems too good to be true, it probably is. Developing a high-quality book can take hundreds of hours of work and requires a high level of expertise—don’t be tempted by third-rate writers who market “affordable” prices.</li>
<li><b>Who owns the rights to the book?</b> It’s your book, which means you should own the rights to your content. Be wary of ghostwriters asking for partial rights to your material.</li>
<li><b>What is the payment arrangement?</b> You should always pay for ghostwriting services in installments and never 100% upfront. After the initial deposit, you should make subsequent payments only if you’re happy with what the ghostwriter has written up to that point. You should have clearly defined milestones for payments to ensure that you and your writer know what to expect.</li>
<li><b>Who’s in charge?</b> Working with a ghostwriter is a collaborative effort, but you should have the final say on all content decisions. If a writer is unwilling to put your needs and requests first, then it will not be your book—it will be theirs.</li>
<li><b>Should I choose a freelancer or a firm?</b> Beware of working with freelancers. While some may be reputable or qualified, most are overbooked and will not make good on the promises they make to you. Also, if anything should go wrong (whether it’s an issue between you and the writer, or a health concern), you will be stuck in the awkward and frustrating position of reclaiming your expenditures and then finding a replacement writer. With a firm like ours, we replace the writer free of charge to ensure the process moves forward efficiently and effectively.</li>
<li><b>What&#8217;s their reputation?</b> Be sure the writer or firm has worked on books that have been published by major publishing houses. Ideally, the writing service you choose will have a robust staff of bestselling writers, experienced editors, and a wide range of connections to literary agents and publishers.</li>
